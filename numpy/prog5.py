import numpy as np

#Creating an empty array
a = np.empty(4)

# A 1-D array is created with all zeros
arr = np.zeros(10)
print(arr)

# Array of mxn 
abc = np.zeros((3,4))
print(abc)

# A 1-D array is created with all ones
xyz = np.ones(10)
print(xyz)

#Diagonal elements are ones or Identity matrix
b = np.eye(3,5)
print(b)
